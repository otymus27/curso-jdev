package aula_11;

public class SequenciaEscape {

	public static void main(String[] args) {
		
		//"Hello, World!" 
		System.out.println("Formatação para output de texto:");
		System.out.println("\"Formatação com aspas duplas!\"");
		System.out.println("\"Formatação com aspas simples!\"");
		System.out.println("Formatação para barra invertida - 1\\4");
		System.out.println("Formatação para barra \b invertida - 1\\4");
		System.out.print("Formatação usando \\n Fábio \nAlencar Rocha");
		
		/*Sequencias de Escape - formatação de saídas de texto
		 * \t - tab
		 * \b - backspace
		 * \n - nova linha
		 * \r - retorno de carro
		 * \f - avano de página
		 * \' - aspas simples
		 * \" - aspas duplas
		 * \\ - barra invertida
		 * 
		 * 
		 * */
	}

}
