package aula_13;

public class OperadoresRelacionais {
	public static void main(String[] args) {
		int a = 15;
		int b = 10;
				
								
		System.out.println("Operadores relacionais:");		
		System.out.println(+a +" = "+b+" => "+(a==b) );
		System.out.println(+a +" != "+b+" => "+(a!=b) );
		System.out.println(+a +" > "+b+" => "+(a>b) );
		System.out.println(+a +" < "+b+" => "+(a<b) );
		System.out.println(+a +" >= "+b+" => "+(a>=b) );
		System.out.println(+a +" <= "+b+" => "+(a<=b) );
		
		
	}

}
